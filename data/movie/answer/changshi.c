mapping query_question()
{
        mapping select;
        int i;
        string *ban;

        if (file_size("/data/movie/caipiao/answerban")>1)
                ban = explode(read_file("/data/movie/caipiao/answerban"),"*");
        select = ques[random(sizeof(ques))];
        for(i=0;i<sizeof(ban);i++)
        {
        if (select["long"] == ban[i] ) return query_question();
        }
        return select;
}

